#include "ft_printf.h"

int	upnbr(unsigned int n)
{
	char	*base;
	int		i;
	char	c[11];
	int		a;

	a = 0;
	i = 0;
	base = "0123456789";
	if (n == 0)
		a += wrtc(base[0]);
	while (n)
	{
		c[i++] = base[n % 10];
		n /= 10;
	}
	i--;
	while (i >= 0)
	{
		a += wrtc(c[i]);
		i--;
	}
	return (a);
}
